function updateLanguage(lang) {
    document.querySelectorAll("[data-lang]").forEach(element => {
        element.textContent = translations[lang][element.dataset.lang] || element.textContent;
    });
    document.querySelectorAll("[data-lang-placeholder]").forEach(element => {
        element.placeholder = translations[lang][element.dataset.langPlaceholder] || element.placeholder;
    });
}